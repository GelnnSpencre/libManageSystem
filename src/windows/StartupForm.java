    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        if (studentDashboard == null && openAdminDashboards.size() == 0) {
            System.exit(0);
            return;
        }
        if (studentDashboard != null) {
            if (studentDashboard.isVisible()) {
                JOptionPane.showMessageDialog(null, "Please log out all accounts first", "Library Management System", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        if (openAdminDashboards.size() > 0) {
            JOptionPane.showMessageDialog(null, "Please log out all accounts first", "Library Management System", JOptionPane.ERROR_MESSAGE);
            return;           
        }
        System.exit(0);
    }//GEN-LAST:event_formWindowClosing

