    private void adminBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminBttnActionPerformed
        this.registerAdmin = new RegisterAdmin();
        this.adminLogin = new AdminLogIn();
        if (registerAdmin.getPsdb().getAllAdmins().size() == 0) {
            JOptionPane.showMessageDialog(null, "The Library System currently has no registered Admin", "Library Management System", JOptionPane.INFORMATION_MESSAGE);
            registerAdmin.setStartForm(this);
            registerAdmin.setVisible(true);
            this.adminBttn.setEnabled(false);
        } else {
            this.adminLogin.setStartForm(this);
            this.adminLogin.setVisible(true);
            adminBttn.setEnabled(false);
        }
    }//GEN-LAST:event_adminBttnActionPerformed

